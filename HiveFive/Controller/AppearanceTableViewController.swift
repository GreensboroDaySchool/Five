private let reuseIdentifier = "cell2"
class AppearanceTableViewController: UITableViewController {
    
    var items = [
        "Themes",
        "User Interface",
        "Color Scheme",
        "Profiles"
    ]

    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
